export const PreloaderLayout = ({ renderFromServer }) => {
  const [showPreloader, setShowPreloader] = useState(!!renderFromServer);
  const [renderPreloader, setRenderPreloader] = useState(!!renderFromServer);

  useEffect(() => {
    if (renderPreloader) {
      setTimeout(() => {
        setShowPreloader(false);

        setTimeout(() => {
          setRenderPreloader(false);
        }, 400);
      }, 1500);

      return () => {
        setShowPreloader(false);
      };
    }
  }, []);

  return (
    renderPreloader && (
      <div
        className={clsx(
          'flex justify-center items-center bg-main-bg h-screen w-screen z-[9999999] fixed top-0 right-0 left-0 bottom-0 transition-all duration-300',
          {
            'opacity-0': !showPreloader,
            invisible: !showPreloader,
          },
        )}
      >
        <div className="flex w-1/2 sm:w-3/4">
          <Lottie
            onComplete={() => console.log('wtf')}
            eventListeners={[
              {
                eventName: 'complete',
                callback: () => {
                  console.log('hello');
                  setShowPreloader(false);
                },
              },
            ]}
            options={{
              autoplay: true,
              animationData: lottieData,
              rendererSettings: {
                progressiveLoad: true,
              },
            }}
          />
        </div>
      </div>
    )
  );
};
