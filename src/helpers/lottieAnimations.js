export const animationCardMapper = (percent, round, isMobile, isFreeze, isFirstOnFirstLine, isCompleteAllClones) => {
  if (round === ROUND_1) {
    if (isMobile) {
      if (isCompleteAllClones) {
        return mobileGreen(percent);
      }

      if (isFirstOnFirstLine) {
        return mobileGray(percent);
      }

      return isFreeze ? mobileFreeze(percent) : mobileRoundOneWaves(percent);
    }

    if (isCompleteAllClones) {
      return desktopGreen(percent);
    }

    if (isFirstOnFirstLine) {
      return desktopGray(percent);
    }

    return isFreeze ? desktopFreeze(percent) : roundOneWaves(percent);
  }

  if (round === ROUND_2) {
    if (isMobile) {
      if (isCompleteAllClones) {
        return mobileGreen(percent);
      }

      if (isFirstOnFirstLine) {
        return mobileGray(percent);
      }

      return isFreeze ? mobileFreeze(percent) : mobileRoundTwoWaves(percent);
    }

    if (isCompleteAllClones) {
      return desktopGreen(percent);
    }

    if (isFirstOnFirstLine) {
      return desktopGray(percent);
    }

    return isFreeze ? desktopFreeze(percent) : roundTwoWaves(percent);
  }
};

